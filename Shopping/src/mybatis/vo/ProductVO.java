package mybatis.vo;

public class ProductVO {
	private String p_key, //기본키(제품 번호)
				p_category, //카테고리
				p_name, // 제품명
				p_content, //제품 설명
				p_thumbnail, //작은 이미지(제품 목록창에서 사용)
				p_image, //큰 이미지(제품 상세보기에서 사용)
				p_date, //등록일
				p_price, //단가
				p_quant; //수량

	public String getP_key() {
		return p_key;
	}

	public void setP_key(String p_key) {
		this.p_key = p_key;
	}

	public String getP_category() {
		return p_category;
	}

	public void setP_category(String p_category) {
		this.p_category = p_category;
	}

	public String getP_name() {
		return p_name;
	}

	public void setP_name(String p_name) {
		this.p_name = p_name;
	}

	public String getP_content() {
		return p_content;
	}

	public void setP_content(String p_content) {
		this.p_content = p_content;
	}

	public String getP_thumbnail() {
		return p_thumbnail;
	}

	public void setP_thumbnail(String p_thumbnail) {
		this.p_thumbnail = p_thumbnail;
	}

	public String getP_image() {
		return p_image;
	}

	public void setP_image(String p_image) {
		this.p_image = p_image;
	}

	public String getP_date() {
		return p_date;
	}

	public void setP_date(String p_date) {
		this.p_date = p_date;
	}

	public String getP_price() {
		return p_price;
	}

	public void setP_price(String p_price) {
		this.p_price = p_price;
	}

	public String getP_quant() {
		return p_quant;
	}

	public void setP_quant(String p_quant) {
		this.p_quant = p_quant;
	}


	
			
}




